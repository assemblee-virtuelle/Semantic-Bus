'use strict';

var mongoose = require('mongoose');

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

var CacheSchema = mongoose.Schema({
  frag: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "fragment"
  },
  date: {
    type: Date,
    default: Date.now
  },
  history: [{
    date: {
      type: Date,
      default: Date.now
    },
    frag: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "fragment"
    }
  }],
});

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

module.exports = CacheSchema;
