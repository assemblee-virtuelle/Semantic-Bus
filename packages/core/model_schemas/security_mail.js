'use strict';

const mongoose = require('mongoose');

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------


const SecureMail = mongoose.Schema({

  timeStamp: {
    type: Date,
    default: Date.now,
    required: true
  },
  userMail: {
    type: String,
    required: true
  },
  token: {
    type: String,
    required: true
  }
});
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

module.exports = SecureMail;
